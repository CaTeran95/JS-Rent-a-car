class Person {
	constructor (name, gender, birthday, id, role, user, password, email, phoneNumber, address) {
		this.name = name;
		this.gender = gender;
		this.birthday = birthday;
		this.id = id;
		this.role = role;
		this.user = user;
		this.password = password;
		this.email = email;
		this.phoneNumber = phoneNumber;
		this.address = address;
	}
}